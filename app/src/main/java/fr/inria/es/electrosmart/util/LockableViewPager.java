package fr.inria.es.electrosmart.util;

import android.content.Context;
import android.util.AttributeSet;
import android.view.MotionEvent;

import androidx.viewpager.widget.ViewPager;

/**
 * Extends the ViewPager class to add the capability to disable swipe behavior.
 */
public class LockableViewPager extends ViewPager {

    private boolean mSwappable;

    public LockableViewPager(Context context) {
        super(context);
    }

    public LockableViewPager(Context context, AttributeSet attrs) {
        super(context, attrs);
        mSwappable = true;
    }

    @Override
    public boolean onTouchEvent(MotionEvent event) {
        if (mSwappable) {
            return super.onTouchEvent(event);
        }

        return false;
    }

    @Override
    public boolean onInterceptTouchEvent(MotionEvent event) {
        if (mSwappable) {
            return super.onInterceptTouchEvent(event);
        }

        return false;
    }

    /**
     * Disables or enable swipe behavior.
     *
     * @param swappable true to enable or false to disable.
     */
    public void setSwappable(boolean swappable) {
        mSwappable = swappable;
    }
}
